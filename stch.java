public class stch {
        public static void main(String[] args) {
            // Declare a variable for the switch expression
            int Pin = 9;
    
            // Switch expression
            switch (Pin) {
                case 1:
                    System.out.println("The Pin is 1.");
                    break;
                case 2:
                    System.out.println("The Pin is 2.");
                    break;
                case 3:
                    System.out.println("The Pin is 3.");
                    break;
                case 4:
                    System.out.println("The Pin is 4.");
                    break;
                case 5:
                    System.out.println("The Pin is 5.");
                    break;
                case 6:
                    System.out.println("The Pin is 6.");
                    break;
                case 7:
                    System.out.println("The Pin is 7.");
                    break;
                default:
                    System.out.println("Invalid Pin.");
            }
        }
}
